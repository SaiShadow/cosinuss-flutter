/// An enumeration representing the types of sessions in the Pomodoro Timer.
///
/// Each session type has a specific purpose and duration.
enum Session {
  /// Work session:
  /// Focused time for productivity, where tasks or goals are actively worked on.
  work,

  /// Short Break session:
  /// A brief rest period to recharge, helping to maintain focus and reduce fatigue before the next work session.
  shortBreak,
}
